            function login() {
                // Reset error messages
                
                
                $('.error-message').hide();
                $('.success-message').hide();
                
                $('#successMessage1').html('');
                $('#errorMessage1').html('');
        
                // Validate input fields
                $('.custom-form input').removeClass('error-input');
                var isValid = false;
                
               
                if ($('#logEmail').val().trim() === '' || !isValidEmail($('#logEmail').val().trim())) {
                    $('#logEmail').addClass('error-input');
                    if(!isValid) $('#logEmail').focus();
                    isValid = true;
                }
              
                if ($('#logPassword').val().trim() === '') {
                    $('#logPassword').addClass('error-input');
                    if(!isValid) $('#logPassword').focus();
                    isValid = true;
                }
                
                if(isValid){
                    $('#btnRegister').removeClass('hide');
                    return false;
                } 
                
                $('#btnLogIn').addClass('hide');
                
                 var postData = {
                    function: 'User',
                    method: "login",
                    'email':$('#logEmail').val(),
                    'password':$('#logPassword').val(),
                  }
              
                $.ajax({
                    url: '/admin/ajaxHandler.php',
                    type: 'POST',
                    data: postData,
                    dataType: "json",
                    success: function (data) {
                        console.log(data);
                        console.log(data.status);
                        //called when successful
                        if (data.status == 1) {
                            $('#successMessage1').html(data.data);
                          $('#successMessage1').show();
                        }else if (data.status == 0){
                             $('#errorMessage1').html(data.data);
                          $('#errorMessage1').show();
                          $('#btnLogIn').removeClass('hide');
                        }else{
                            location.reload();
                        }
                       
                    },
                    error: function (x,h,r) {
                    //called when there is an error
                        console.log(x);
                        console.log(h);
                        console.log(r);
                        $('#btnLogIn').removeClass('hide');
                       
                    }
                });
                
                
                
        
               
            }
            
            
        
            
            
